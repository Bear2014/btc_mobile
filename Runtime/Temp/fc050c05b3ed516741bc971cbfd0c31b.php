<b>9.什么是矿工和矿池，难度是指什么？</b><span style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;"></span> 
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>矿工：</b>　　是指这些尝试创建块并添加到区块链里面的电脑。（也指一些挖矿软件）。矿工的工作将受到bitcoin协议的奖励，一旦发现一个有效的块，系统自动奖励50个新产生的比特币（奖励约每4年减半一次，2012年12月将是第一次减半）。这也是比特币的来源。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>矿池：</b>　　由于产生一个有效块的概率很小（奖励50BTC）， 矿池提供让矿工一起创建块并均匀分割&nbsp;的利润的平台，这样能保证矿工有一个可靠的收入。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>难度：</b>　　是指产生一个新块的困难程度。它会自动调整，以确保全网络平均10分钟能找到一个有效的块。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<a name="10"></a> 
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
