<b>3.政府对比特币的态度是怎么样的？</b><span style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;"></span> 
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>美国：</b>&nbsp;　　2013年5月，美国财政部金融犯罪执法系统FinCEN发布了虚拟货币个人管理《条例》。阐述了具备“可转换”性的虚拟货币。这种类型的虚拟货币具有实际货币的同等价值，或是实际货币的替代品。&nbsp;<br />
　　2013年5月，美国国土安全局通告称，电子货币交易网Mt. Gox首席执行官馬克·卡普利斯（Mark Karpeles），没能如实告知他在富国银行开设的账户是用作货币兑换，并查封比特币交易账户。随后，Mt. Gox公布交易认证机制——用户需提供政府承认的身份证和债务信息。&nbsp;<br />
　　2013年5月，加州政府金融（DFI）机构资深大律师，保罗·克莱顿，用信函通知《比特币基金会》终止货币交易活动。比特币基金会律师派崔克·默克（Patrick Murck）表示，加州DFI似乎在试探性地，用这封信向比特币经济生态发送一个警告。&nbsp;<br />
