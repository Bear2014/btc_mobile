<b>2.比特币的产生原理是什么？</b><span style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;"></span> 
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　从比特币的本质说起，比特币的本质其实就是一堆复杂算法所生成的特解。特解是指方程组所能得到无限个(其实比特币是有限个)解中的一组。而每一个特解都能解开方程并且是唯一的。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　以人民币来比喻的话，比特币就是人民币的序列号，你知道了某张钞票上的序列号，你就拥有了这张钞票。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　而挖矿的过程就是通过庞大的计算量不断的去寻求这个方程组的特解，这个方程组被设计成了只有 2100 万个特解，所以比特币的上限就是 2100 万。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
