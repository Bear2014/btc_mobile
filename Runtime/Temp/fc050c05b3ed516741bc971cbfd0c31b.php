<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>8.比特币网络及区块链怎么定义？</b>
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>比特币网络：</b>&nbsp;　　是指计算机连接的网络，通过它广播比特币交易信息和 维护全网区块链。有时特指矿机。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>区块链：</b>　　是指包含所有交易的公共链表，以确保每个人都知道具体比特币属于谁。所有更新完的的网络上的节点保持一个区块链的副本。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>块：</b>　　是指区块链里的最小独立单元。每个块中包含的以前的的块的 哈希 （所以人们不能通过沿区块链不能取出块或者改变块，，而是其他块接受），因此可以在网络中找到许多未经证实的交易，一个这样的交易称为nonce。创建一个块的人必须找到一个随机数，使得该块的散列是低于某一阈值（目标），这只能通过尝试所有的nonce一前一后，直到发现，产生一个理想的散列，是较低的目标是更难。这么故意使块 创建过程复杂而困难的 ，是为了防止有人花费比特币后然后生成不包含的交易区块，删除该记录，这样他就能通一些BTC使用两次（即双重支付攻击）。当一个有效的块被创建，它将传遍整个网络，决定下一个块的产生。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>起源块：</b>　　在2009年1月4日公布的区块链的第一个块。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>未确认交易：</b>　　是指没有存储区块的交易。确认的交易是指永久称为区块链里面一部分的交易。'6个确认“是指交易在一个块里面，它后面的区块链中有5个块，这就对交易是合法的提供了足够的保证。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
