<b>5.什么是比特币交易确认？</b><span style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;"></span> 
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　比特币的交易数据被打包到一个“数据块”或“区块”（block）中后，交易就算初步确认了。当区块链接到前一个区块之后，交易会得到进一步的确认。在连续得到6个区块确认之后，这笔交易基本上就不可逆转地得到确认了。比特币对等网络将所有的交易历史都储存在“区块链”（blockchain）中。区块链在持续延长，而且新区块一旦加入到区块链中，就不会再被移走。区块链实际上是一群分散的用户端节点，并由所有参与者组成的分布式数据库，是对所有比特币交易历史的记录。中本聪预计，当数据量增大之后，用户端希望这些数据并不全部储存自己的节点中。为了实现这一目标，他采用引入散列函数机制。这样用户端将能够自动剔除掉那些自己永远用不到的部分，比方说极为早期的一些比特币交易记录。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　确认一项交易的过程，是由解决一系列计算难题的工作量证明机制来实现的。工作量证明机制要求电脑的计算能力为某个有限值的情况下，需要运算一定的时间才能解决，这就使得攻击者无法重写交易历史，除非他能够拥有比其比特币对等网络系统更强大的计算能力，从而能以更快地速度产生区块链。比特币基金会和参与者及时发现过山寨比特币区块链，他们在达成共识后，劝阻了开发者停止开发和使用山寨产品，避免了一个比特币份额在两个区块链两次使用的可能。工作量证明机制的难度由系统自动调节，所以新区块的生成平均需时10分钟。整个比特币P2P对等网络的节点都会自动检测交易和区块的有效性，并忽略任何违背规则的交易和区块，比如那些产生错误数量的区块，或多次发送同一份额比特币的交易行为。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　参与处理区块的用户端可以得到一定量新发行的比特币，以及相关的交易手续费。为了得到这些新产生的比特币，参与处理区块的用户端需要付出大量的时间和计算力，这个过程非常类似于挖矿，因此中本聪将数据处理者命名为“矿工”，将数据处理活动称之为“挖矿”。这些新产生出来的比特币可以报偿系统中的数据处理者，他们的计算工作为比特币对等网络的正常运作提供了保障。通过挖矿，比特币系统也向全球经济注入了革命性货币。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
