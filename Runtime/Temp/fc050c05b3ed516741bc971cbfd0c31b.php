<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　比特币（英语：Bitcoin，简写：BTC，货币符号：฿）是一种用户自治的、全球通用的加密电子货币。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　其概念由中本聪（化名）在2008年提出。随后，他以公开对等、共识主动性的理念为基准，把密码学原理、对等网络技术和开源软件相结合，开发出能自我完善的、免费的比特币应用体系；2009年1月3日，比特币问世。经过比特币社区成员们不懈地努力，其应用系统不断完善，从而使认可、使用、参与的个人、组织和企业在全球迅速增加；同时，重视它的国家和其权威机构也越来越多，使之成为一个适应于互联网时代的、独立于传统金融系统的新型金融生态体系。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　与传统货币不同，比特币运行机制不依赖中央银行、政府、企业的支持或者信用担保，而是依赖对等网络中种子文件达成的网络协议，去中心化、自我完善的货币体制，理论上确保了任何人、机构、或政府都不可能操控比特币的货币总量，或者制造伪币。它的货币总量按照设计预定的速率逐步增加，增加速度逐步放缓，并最终在2140年达到2100万个的极限。比特币应用体系的防伪、抗通胀和安全，也不靠国家来维护，而是靠对等网络技术和参与者的共识主动性合作。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
