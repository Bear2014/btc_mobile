<p style="font-family:微软雅黑;color:#FFFFFF;font-size:medium;background-color:#1F0909;">
	<span style="font-family:&quot;color:#333333;font-size:10px;"><strong>　　二、资金管理。资金管理在股市上也许算不了什么重要的原则，但在期货市场却是首要的投资管理原则。即便是最成功的交易者也不能保证每一次投资都是成功的，期货市场需要的是盈利交易中盈利额大于亏损交易中的亏损额来取得成功的，而并非寄希望于百分之百的成功率。由于期货高杠杆机制的特点，如果只要有一次不成功的投资，加上没有明确的交易计划来止损，就可能亏损掉所有资金。因此，资金管理在期货市场上就成为首要的操作原则。</strong></span>
</p>
<p style="font-family:微软雅黑;color:#FFFFFF;font-size:medium;background-color:#1F0909;">
