<b>6.什么是比特币51%攻击？</b><span style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;"></span> 
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　如果我有整个网络51%的计算速度，我可以计算出一个这样的区块链，包含我发送所有的比特币到我的私人账户上这个交易信息。这个区块链的长度为10，但是我不向网络广播。同时，我把所有的比特币在交易市场换成美元并提取出来。这笔交易记录在正常的那个区块链中。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　当我的美元提取正在进行中的时候，那个正常的区块链的长度是9，而我的区块链长度是10。现在我向网络广播出去，然后观察，网络会确认我的区块链是正确的。但是美元已经被我提取了，损失的是交易市场。我也可以用这种方式拿比特币买任何东西。如果这样的事情发生个几次，对比特币将是致命的，至少会严重的伤害人们对这个系统的信任度。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	　　一提到对比特币的攻击，大部分人想到的就是51%攻击。所谓51%攻击，就是利用比特币使用算力作为竞争条件的特点，使用算力优势撤销自己已经发生的付款交易。比特币发动51%攻击具体思路,大致如下：
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>准备工作：</b>&nbsp;　　1) 既然是51%攻击，就必须首先掌握足够的算力，无论是控制矿池，还是利用其它计算资源，总之必须使你的算力领先与现在网络总算力，领先的幅度越大，成功的可能性越高；<br />
　　2) 拿到足够的BTC作为筹码，无论是自己挖到的，还是从任何渠道买的，都可以；
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>攻击步骤：</b>&nbsp;　　1) 将手中的BTC充值各大交易所，然后卖掉，提现；或者也可以直接卖给某人或某一群人；<br />
　　2)运用手中的算力，从自己对外付款交易之前的区块开始，忽略自己所有对外的付款交易，重新构造后面的区块，利用算力优势与全网赛跑，当最终创建的区块长度超过原主分支区块，成为新的主分支，至此，攻击完成；
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>攻击结果：</b>&nbsp;　　由于撤销了所有对外付款交易，等于收回来所以已卖掉的比特币。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>可行性评估：</b>&nbsp;　　现在比特币全网算力差不多90T，而且还会快速增长，现在看来只有现在的几大矿池联合，才具有发动51%攻击的实力，普通个人或机构实施此攻击的可能性越来越小。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	过去几年比特币网络的算力悄无声息的增长到了无比之大，这大大增加了比特币成功的可能性。反之也可以解释为什么山寨币不靠谱，太容易被51%攻击啦。在依赖密码学的数字货币领域，先发优秀是非常明显的。
</p>
