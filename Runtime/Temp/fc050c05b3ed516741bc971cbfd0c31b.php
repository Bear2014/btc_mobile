	<p style="color:#222222;">
		在Corda开放资源的同时，它被提交给了超级账本，并加入了Fabric和英特尔的Sawtooth项目，以及其他的协议。虽然，每家公司的运行方式不同，但是所有的设计都是为了协助金融交易，所以每家公司的创建标准也大不相同。
	</p>
	<p style="color:#222222;">
