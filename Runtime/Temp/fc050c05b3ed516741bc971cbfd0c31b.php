<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>10.比特币的技术风险主要是什么？</b>
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>51%攻击：</b>　　是指具有超强的挖矿计算能能力，并获得整个网络的控制权（至少需要占全网运算能力的51%），可以修改之前的交易记录。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>双重支付：</b>　　是指将同一个比特币发送2次。矿工的工作通常可以避免这种情况的发生。但是如果用户接受未确认交易或者和51%攻击相结合，双重支付也是有可能的发生的。
</p>
