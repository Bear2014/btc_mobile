<b>11.比特币交易市场相关知识有哪些？</b><span style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;"></span> 
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>兑换：</b>　　是指提供人们用其他货币买卖比特币的平台，比特币交易网（www.btctrade.com）力求打造最安全快捷的平台。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>ask</b>　　是指在某个交易所中目前愿意卖出的价格，bid是指目前市场中最高愿意买进的价格，ask-bid差价是指ask和bid之间的差价。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>交易量：</b>　　是指一定时间内的比特币的交易量。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>市场深度：</b>　　是指在一定时间内的人们在交易市场已经挂单卖出而没有卖出的比特币的数量（因为没人愿意接受这些价格）。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>投机者：</b>　　是指这些尝试低买高卖来赚钱的人.对冲是指在利用不同交易平台间差价而获利 的活动。波段是指依靠预测短期价格波动 高频率的交易而获利的活动。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>泡沫：</b>　　当人们对Bitcoin的未来 价格持乐观态度，并买比特币，导致Bitcoin的价格迅速涨上去了，继续循环，直到泡沫“啪啪”的爆开，市场崩溃价格回落（市场的校正）。目前最大的泡沫发生在2011年4月至6月，导致价格从0.75美元迅速增至30美元，并急剧下降至2美元。不久前2012年8月泡沫再次出现，从9美元急剧1增加至15.5美元，并跌至9美元。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>保证金交易：</b>　　是一种极具风险的投机交易（使用杠杆）。你可以使用借来的钱交易比特币，冒个更大的风险来获取更高的利润。比如使用5倍的杠杆，20%强制结算，你预测比特币价格将下降，你可以卖出一定量比特币（即使你没有比特币，只要你拥有足够的保证金），如果比特币价格下跌，你可以买进对应数量的比特币（获利），如果比特币上涨（赔钱），你可以买进相应数量的比特币，损失的钱从保证金里扣除，而当价格上涨超过你买入价格的20%（即强制结算价格），会被强制清仓，从保证金里扣除损失资。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>Satoshi：</b>　　以比特币创始人中本聪（Satoshi Nakamoto）名字命名的比特币单位。1 satoshi&nbsp;等于百万分之一比特币，也是目前能流通的最小比特币单位。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>中介服务：</b>　　通过认证的交易者，买方将比特币转移到可信第三方中介，在确保交易完成后，这些比特币转移卖方。(类似支付宝)。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>签名加密：</b>　　是比特币的一个高级特性，可以和0信任的人进行比特币交易。在未来，比特币将采用2个或多个的签名加密。从技术角度讲，所有的比特币交易使用签名加密，但该术语通常只用在非常规交易的讨论。
</p>
