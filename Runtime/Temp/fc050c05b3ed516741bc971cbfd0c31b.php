<b>7.比特币相关的基础术语有哪些？</b><span style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;"></span> 
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>比特币客户端（钱包）：</b>&nbsp;　　是处理发送和接收比特币的软件。最流行的是从bitcoin官网下载的客户端。你还有其他一些选择。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>钱包：</b>&nbsp;　　有两层含义，1、是指比特币客户端（客户端一般指桌面客户端；钱包一般指轻量级的客户端或在线钱包）；2、是指存储比特币地址和私钥的文件。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>比特币地址：</b>&nbsp;　　是一个诸如 “13ignD31FysQbaBBVJUzffcQoFxxEuEcbE”的字符串，你可以从接收方获得。用客户端创建一个地址同时生成对应的私匙。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>私匙：</b>&nbsp;　　在比特币的背景下，私匙 是和对应地址相关联的（从技术上讲，地址是私钥 所对应的公钥 的哈希值），并且存储在底层，并允许您转移当前地址所拥有的比特币。请注意，因为Bitcoin的使用的ECDSA&nbsp;加密算法，它能够生成从私钥生成对应的公钥和地址。
</p>
<p style="color:#333333;font-family:微软雅黑, 宋体, Arial;font-size:14px;background-color:#FFFFFF;">
	<b>交易：</b>&nbsp;　　交易是指一个通知整个比特币网络的信息，告诉全网交易的发生，允许接收者使用这些钱，防止发送者双重支付。
</p>
